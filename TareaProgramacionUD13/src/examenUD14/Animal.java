package examenUD14;

public class Animal {
	
	private String nombre;
	private String genero;
	private String especie;
	private int edad;
	
	public Animal(String nombre, String genero, String especie, int edad) {
		this.nombre = nombre;
		this.genero = genero;
		this.especie = especie;
		this.edad = edad;
	}

}
